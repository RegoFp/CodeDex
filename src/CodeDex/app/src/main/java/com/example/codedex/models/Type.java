package com.example.codedex.models;

public class Type {

    private String slot;
    private String name;

    public String getSlot() {
        return slot;
    }

    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;

    }
}
